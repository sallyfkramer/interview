package com.payit.problems.models;

public class Duplicates {
    private int possibleDuplicates;
    private int fullDuplicates;

    public int getPossibleDuplicates() {
        return possibleDuplicates;
    }

    public void setPossibleDuplicates(int possibleDuplicates) {
        this.possibleDuplicates = possibleDuplicates;
    }

    public int getFullDuplicates() {
        return fullDuplicates;
    }

    public void setFullDuplicates(int fullDuplicates) {
        this.fullDuplicates = fullDuplicates;
    }
}
